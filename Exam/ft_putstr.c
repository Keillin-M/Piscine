#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
}

int	main(int argc, char **argv)
{
	(void)argc;
	*argv = "Hello World";

	ft_putstr(*argv);
	return (0);
}
